        public static double ProductOddElements(double[] arr) // 1
        {
            double result = 1;
            for (int i = 1;
                i < arr.Length;
                i += 2)
            {
                result *= arr[i];
            }
            return result;
        }

        public static double[] CyclicRightShift(double[] arr, int shift)
        {
            if (shift == 0 || shift == arr.Length)
            {
                return arr;
            }
            if (shift > arr.Length)
            {
                shift = shift % arr.Length;
            }

            double[] shiftedArr = new double[arr.Length];

            for (int i = arr.Length - 1;
                i >= 0;
                i--)
            {
                if (i + shift >= arr.Length)
                {
                    shiftedArr[i + shift - arr.Length] = arr[i];
                }
                else
                {
                    shiftedArr[i + shift] = arr[i];
                }
            }

            return shiftedArr;
        }

        public static int FromFractionToInt(int radix, string fraction)
        {
            int result = 0;
            for (int i = 0;
                i < fraction.Length;
                i++) { 
                if (fraction[i] == '.' || fraction[i] == ',')
                {
                    continue;
                }

                if (int.Parse(Convert.ToString(fraction[i])) >= radix)
                {
                    return -1;
                }
            }
            
            if (fraction.Contains("."))
            {
                string[] words = fraction.Split(new char[] { '.' });
                result = int.Parse(words[0]);
            }
            else if (fraction.Contains(","))
            {
                string[] words = fraction.Split(new char[] { ',' });
                result = int.Parse(words[0]);
            }

            return result;
        }
